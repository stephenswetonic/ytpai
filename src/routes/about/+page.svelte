    <div class="my-1">SentenceMixer AI is a free tool for mashing together words from audio and video into a single clip. Think Youtube poop/SFM style but without the manual editing.</div>
    <div>All files are deleted from the server daily.</div>
    <div class="my-1">Voice analysis done via WhisperX on my local server. Clip generation done on AWS Lambda. My goal is to keep this free to use and without registration.</div>

